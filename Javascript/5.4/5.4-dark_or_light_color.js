let color = "Brown"

function whichColor(color) {
    switch (color) {
        
        case color = "yellow":
            console.log("Light color");
            break;
        case color = "pink":
            console.log("Light color");
            break;
        case color = "orange":
            console.log("Light color");
            break;
        case color = "blue":
            console.log("Dark color");
            break;
        case color = "purple":
            console.log("Dark color");
            break;
        case color = "brown":
            console.log("Dark color");
            break;
        default:
            console.log("Unknown color")
    }
}

whichColor(color.toLowerCase());